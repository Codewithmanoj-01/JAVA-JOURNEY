package Operators;

public class ComparisonOperators {
    public static void main(String[] args) {
        
        int a = 10;
        int b = 20;

        System.out.println("=== Comparison Operators ===");

        System.out.println("Equal to : " + (a == b)); // Equal to (checks if a and b are equal)
        System.out.println("Not Equal: " + (a != b)); // Not equal to (checks if a and b are not equal)
        System.out.println("Greater Than: " + (a > b)); // Greater than (checks if a is greater than b)
        System.out.println("Less Than: " + (a < b)); // Less than (checks if a is less than b)
        System.out.println("Greater Than or Equal: " + (a >= b)); // Greater than or equal to (checks if a is greater than or equal to b)
        System.out.println("Less Than or Equal: " + (a <= b)); // Less than or equal to (checks if a is less than or equal to b)

    }
    
}
