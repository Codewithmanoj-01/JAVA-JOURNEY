package MultidimensionalArrays;

public class TwoDimensionalArray {

    public static void main(String[] args) {

        int[][] matrix = {
                {10, 20, 30},
                {40, 50, 60}
        };

        System.out.println("Element at [0][0]: " + matrix[0][0]);
        System.out.println("Element at [1][2]: " + matrix[1][2]);
    }
}
